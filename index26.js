function sumArrays3(numbers) {
    let sum = 0;
    for (let i = 0; i < numbers.length; i++) {
        sum += numbers[i];
    }
    return sum;
}
const array3 = [1, 2, 3, 4, 5];
const Totalsum = sumArrays3(array3);
console.log(`Total sum: ${Totalsum}`);
